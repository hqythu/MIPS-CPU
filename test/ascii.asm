START:
	; R0用来保存每次需要输出的字符
	LI		R2	0X20
	LI		R1	0XBF
	SLL		R1	R1	0X0
LOOP_START:
	MFPC	R7
	ADDIU	R7	0X02
	B		TESTW
	NOP
	SW		R1	R2	0X0
	ADDIU	R2	0X1
	; 到大于0x7E的时候结束
	LI		R3	0X7F
	CMP		R2	R3
	BTNEZ	LOOP_START
	NOP
FOREVER:
	NOP
	B 		FOREVER
	NOP

;测试8251是否能写
TESTW:
	LI R6 0x00BF
	SLL R6 R6 0x0000
	ADDIU R6 0x0001
	LW R6 R0 0x0000
	LI R6 0x0001
	AND R0 R6
	BEQZ R0 TESTW     ;BF01&1=0 则等待
	NOP
	JR R7
	NOP
